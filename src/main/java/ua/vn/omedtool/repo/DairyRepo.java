package ua.vn.omedtool.repo;

import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;
import ua.vn.omedtool.entities.Dairy;

import java.util.List;


public interface DairyRepo extends JpaRepository<Dairy, Integer> {
    List<Dairy> findAllByDoctorEmailOrDoctorPhone(String email, String phone);

    @Query("SELECT d FROM Dairy d JOIN fetch d.patientList p where p.id=:id ")
    List<Dairy> findPatientByDairyDate(@Param("id") Integer id);
}












































































































































